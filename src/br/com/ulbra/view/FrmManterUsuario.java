    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) tbUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDao pdao = new UsuarioDao();
        
        for (Usuario u : pdao.read()){
            
            modelo.addRow(new Object[]{
                u.getCodigo(),
                u.getNome(),
                u.getEmail()
                
            });
        } 
       
    }
    
    public void readForDesc(String nome){
        DefaultTableModel modelo = (DefaultTableModel) tbUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDao pdao = new UsuarioDao();
        
        for (Usuario u : pdao.readForDesc(nome)){
            
            modelo.addRow(new Object[]{
                u.getCodigo(),
                u.getNome(),
                u.getEmail()
                
            });
        } 
       
    }
    
    
        
    
      
     
    
